# Program to demonstrate the str.format() method

# using format option in a simple string
print("{}, a computer language for programmers."
      .format("Python"))

# using format option for a value stored in a variable
str = "This program is written in {}."
print(str.format("Python"))

# formatting a string using a numeric constant
print("Hello, This is year {}!".format(2019))



